package javasession;

public class MathOperators {

	public static void main(String[] args) {
		
		System.out.println(10 / 2); // it must be same type
		System.out.println(9 / 2);
		System.out.println(9.0 / 2);
		System.out.println(9 / 2.0);
		System.out.println(9.0 / 2.0);
		// System.out.println(9/0);
		System.out.println(0 / 9);
		System.out.println(0 / 1);
		System.out.println(9 / 0.0);
		System.out.println(9.0 / 0);
		System.out.println(9.0 / 0.0);
		// System.out.println(0/0);// AE
		System.out.println(0.0 / 0.0);
		System.out.println(0.0 / 0);
		System.out.println(0 / 0.0);
		System.out.println(100 / 0.0);
		System.out.println(10 % 2);// Remainder
		System.out.println(9 % 2);
		System.out.println(9.2 % 2);

	}

}
